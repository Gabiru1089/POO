public class Musica {
    private String titulo;
    private float duracao;

    public void setTitulo(String titulo){
        this.titulo = titulo;
    }

    public String getTitulo(){
        return this.titulo;
    }

    public void setDuracao(int duracao){
        this.duracao = duracao;
    }

    public double getDuracao(){
        return this.duracao;
    }
}
